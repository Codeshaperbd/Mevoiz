        //$input = $request->all(); //or bellow codintional statement
        if (trim($request->password) == '') {
            $input = $request->except('password');
        } else {
            $input = $request->all();
            $input['password'] = bcrypt($request->password);
        }

        $user->update($input);
        Session::flash('update_user','User Information Has Been Updated.');
